    private void UpdatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdatebtnActionPerformed
        // TODO add your handling code here:
        String nama_obat = txtNama.getText();
        int stok = Integer.parseInt(txtStok.getText());
        double harga = Double.parseDouble(txtHarga.getText());

        Connection conn = DbConnection.getConnection();
        ObatDAO dao = new ObatDAO(conn);

        if (selectedObatId != -1) {
            // Mode UPDATE
            Obat obat = new Obat(selectedObatId, nama_obat, stok, harga);
            dao.update(obat);
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
        } else {
            // Mode INSERT
            Obat obat = new Obat(nama_obat, stok, harga);
            dao.insert(obat);
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
        }

        clearForm();
        loadTable();


    }//GEN-LAST:event_UpdatebtnActionPerformed

    private void ListObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListObatMouseClicked
        // TODO add your handling code here:
       int baris = ListObat.getSelectedRow();
    if (baris != -1) {
        selectedObatId = Integer.parseInt(ListObat.getValueAt(baris, 0).toString());
        String nama = ListObat.getValueAt(baris, 1).toString();
        String stok = ListObat.getValueAt(baris, 2).toString();
        String harga = ListObat.getValueAt(baris, 3).toString();

        txtNama.setText(nama);
        txtStok.setText(stok);
        txtHarga.setText(harga);
    }

    }//GEN-LAST:event_ListObatMouseClicked

