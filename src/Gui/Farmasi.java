   private void loadTable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama Obat");
    model.addColumn("Sisa Stok");
    model.addColumn("Harga");

    Connection conn = DbConnection.getConnection();
    ObatDAO dao = new ObatDAO(conn);
    List<Obat> list = dao.getAll();

    for (Obat o : list) {
        model.addRow(new Object[] {
            o.getId(),
            o.getNamaObat(),
            o.getStok(),
            o.getHarga()
        });
        ListObat.getColumnModel().getColumn(0).setMinWidth(0);
        ListObat.getColumnModel().getColumn(0).setMaxWidth(0);

    }

    ListObat.setModel(model); // Ganti "ListObat" sesuai nama JTable kamu
}
