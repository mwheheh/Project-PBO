    private void CetakRekambtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CetakRekambtnActionPerformed
        // TODO add your handling code here:
      int selectedRow = DaftarPasienTbl.getSelectedRow();
        if (selectedRow != -1) {
            // Ambil data dari tabel
            String nama = DaftarPasienTbl.getValueAt(selectedRow, 1).toString();
            String tanggalLahir = DaftarPasienTbl.getValueAt(selectedRow, 2).toString();
            String jenisKelamin = DaftarPasienTbl.getValueAt(selectedRow, 3).toString();
            String alamat = DaftarPasienTbl.getValueAt(selectedRow, 4).toString();
            String telepon = DaftarPasienTbl.getValueAt(selectedRow, 5).toString();
            String email = DaftarPasienTbl.getValueAt(selectedRow, 6).toString();

            // Buka RekamMedis dan teruskan data
            RekamMedis rekam = new RekamMedis();
            rekam.setDataPasien(nama, tanggalLahir, jenisKelamin, alamat, telepon, email);
            rekam.setVisible(true);

            // Tutup form ini jika perlu
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Pilih pasien terlebih dahulu!");
        }
             
    }//GEN-LAST:event_CetakRekambtnActionPerformed

