    private void loadTable() {
    DefaultTableModel model;
    String[] kolom = {"ID", "Nama", "Tanggal Lahir", "Jenis Kelamin", "Alamat", "No Telepon", "Email"};
    model = new DefaultTableModel(null, kolom);
    List<Pasien> list = new PasienDAO().getAllPasien();

    for (Pasien p : list) {
        model.addRow(new Object[]{
            p.getId(), p.getNama(),
            new SimpleDateFormat("yyyy-MM-dd").format(p.getTanggalLahir()),
            p.getJenisKelamin(), p.getAlamat(),
            p.getNoTelepon(), p.getEmail()
        });
    }
    DaftarPasienTbl.setModel(model);
}

    

