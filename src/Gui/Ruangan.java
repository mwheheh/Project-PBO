    private void loadTable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nomor Kamar");
    model.addColumn("Status");

    Connection conn = DbConnection.getConnection();
    KamarDAO dao = new KamarDAO(conn);
    List<Kamar> list = dao.getAll();

    for (Kamar k : list) {
        model.addRow(new Object[] {
            k.getId(),
            k.getNomorKamar(),
            k.getKeterangan()
        });
    }

    KamarTbl.setModel(model);
    KamarTbl.getColumnModel().getColumn(0).setMinWidth(0);
    KamarTbl.getColumnModel().getColumn(0).setMaxWidth(0);
}

