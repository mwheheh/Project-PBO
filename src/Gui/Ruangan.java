    private void KamarTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_KamarTblMouseClicked
        // TODO add your handling code here:
        int row = KamarTbl.getSelectedRow();
        if (row != -1) {
        selectedKamarId = Integer.parseInt(KamarTbl.getValueAt(row, 0).toString());
        NoKamar.setText(KamarTbl.getValueAt(row, 1).toString());
        String status = KamarTbl.getValueAt(row, 2).toString();

        if (status.equals("Tersedia")) {
            tersedia.setSelected(true);
        } else {
            tidaktersedia.setSelected(true);
        }
    }
    }//GEN-LAST:event_KamarTblMouseClicked

    private void UpbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpbtnActionPerformed
        // TODO add your handling code here:
        int nomorKamar = Integer.parseInt(NoKamar.getText());
        String keterangan = tersedia.isSelected() ? "Tersedia" : "Tidak Tersedia";

        Connection conn = DbConnection.getConnection();
        KamarDAO dao = new KamarDAO(conn);

        if (selectedKamarId != -1) {
            Kamar kamar = new Kamar(selectedKamarId, nomorKamar, keterangan);
            dao.update(kamar);
            JOptionPane.showMessageDialog(this, "Data kamar diperbarui!");
        } else {
            Kamar kamar = new Kamar(nomorKamar, keterangan);
            dao.insert(kamar);
            JOptionPane.showMessageDialog(this, "Kamar baru ditambahkan!");
        }

        clearForm();
        loadTable();
    }//GEN-LAST:event_UpbtnActionPerformed

    
